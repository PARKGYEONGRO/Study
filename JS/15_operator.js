// 표현식 (연산자, 피연산자)
// 피연산자는 연산자에 의하여 연산이되는 변수나 값
// 산술, 대입. 증감. 비교 .논리 .삼항
// 요번 챕터는 산술연산자에 대하여 배움

// 덧셈 연산자
// 문자열 연결 연산자
let addtion = 20 + 10;
console.log(addtion);
let hello = '안녕하세요,' + '박경로입니다.'
console.log(hello);

// 뺄셈 연산자
let subtraction = 20 - 10;
console.log(subtraction);

// 곱셈 연산자
let multiplication = 10 * 10;
console.log(multiplication);

// 나눗셈 연산자
let division = 10 / 5;
console.log(division);

// 나머지 연산자
let remainder = 10 % 3;
console.log(remainder);